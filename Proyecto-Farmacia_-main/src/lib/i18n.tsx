type Key = keyof typeof DICT["es"];

const LangCtx = createContext<{ lang: Lang; setLang: (l: Lang) => void; t: (k: Key) => string }>({
  lang: "es", setLang: () => {}, t: (k) => k as string,
});

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("es");
  useEffect(() => {
    if (typeof window === "undefined") return;
    const saved = (localStorage.getItem("farmasystem_lang") as Lang | null) ?? "es";
    setLangState(saved);
  }, []);
  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") localStorage.setItem("farmasystem_lang", l);
  };
  const t = (k: Key) => DICT[lang][k] ?? (k as string);
  return <LangCtx.Provider value={{ lang, setLang, t }}>{children}</LangCtx.Provider>;
}

export const useLang = () => useContext(LangCtx);
